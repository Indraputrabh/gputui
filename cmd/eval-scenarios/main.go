// Command eval-scenarios runs the default hint evaluator against each
// JSONL scenario in milestones/eval/scenarios.jsonl and writes a
// per-scenario pass/fail summary, used as the rule-engine accuracy harness.
//
// Usage:
//
//	go run ./cmd/eval-scenarios \
//	    -in milestones/eval/scenarios.jsonl \
//	    -out milestones/eval/results/phase1-scenario-results.jsonl \
//	    -summary milestones/eval/results/phase1-summary.md
package main

import (
	"bufio"
	"encoding/json"
	"flag"
	"fmt"
	"io"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"github.com/indraputrabh/gputui/internal/hints"
	"github.com/indraputrabh/gputui/internal/model"
)

type scenario struct {
	Name          string                  `json:"scenario"`
	GPUs          []model.GPUStat         `json:"gpus"`
	Node          model.NodeStat          `json:"node"`
	HealthSignals []model.GPUHealthSignal `json:"health_signals"`
	ExpectedHints []string                `json:"expected_hints"`
}

type result struct {
	Scenario string   `json:"scenario"`
	Expected []string `json:"expected"`
	Actual   []string `json:"actual"`
	Missing  []string `json:"missing"`
	Extra    []string `json:"extra"`
	Pass     bool     `json:"pass"`
}

func main() {
	code, err := run()
	if err != nil {
		fmt.Fprintf(os.Stderr, "%v\n", err)
		os.Exit(1)
	}
	if code != 0 {
		os.Exit(code)
	}
}

func run() (int, error) {
	in := flag.String("in", "milestones/eval/scenarios.jsonl", "scenarios JSONL input")
	out := flag.String("out", "milestones/eval/results/phase1-scenario-results.jsonl", "per-scenario JSONL output")
	summary := flag.String("summary", "milestones/eval/results/phase1-summary.md", "Markdown summary output")
	flag.Parse()

	fh, err := os.Open(*in)
	if err != nil {
		return 0, fmt.Errorf("open input: %w", err)
	}
	defer fh.Close()

	if err := os.MkdirAll(filepath.Dir(*out), 0o755); err != nil {
		return 0, fmt.Errorf("mkdir out: %w", err)
	}
	if err := os.MkdirAll(filepath.Dir(*summary), 0o755); err != nil {
		return 0, fmt.Errorf("mkdir summary: %w", err)
	}

	outFh, err := os.Create(*out)
	if err != nil {
		return 0, fmt.Errorf("create out: %w", err)
	}
	defer outFh.Close()

	evaluator := hints.DefaultEvaluator()
	scanner := bufio.NewScanner(fh)
	scanner.Buffer(make([]byte, 0, 64*1024), 1024*1024)

	var results []result
	for scanner.Scan() {
		line := strings.TrimSpace(scanner.Text())
		if line == "" {
			continue
		}
		var sc scenario
		if err := json.Unmarshal([]byte(line), &sc); err != nil {
			return 0, fmt.Errorf("parse line: %w\n%s", err, line)
		}
		snap := model.Snapshot{
			GPUs:          sc.GPUs,
			Node:          sc.Node,
			HealthSignals: sc.HealthSignals,
		}
		hintsOut := evaluator.Evaluate(snap)
		actual := make([]string, 0, len(hintsOut))
		seen := map[string]struct{}{}
		for _, h := range hintsOut {
			if _, dup := seen[h.Name]; dup {
				continue
			}
			seen[h.Name] = struct{}{}
			actual = append(actual, h.Name)
		}
		sort.Strings(actual)
		expected := append([]string(nil), sc.ExpectedHints...)
		sort.Strings(expected)

		r := result{
			Scenario: sc.Name,
			Expected: expected,
			Actual:   actual,
			Missing:  diff(expected, actual),
			Extra:    diff(actual, expected),
		}
		r.Pass = len(r.Missing) == 0 && len(r.Extra) == 0
		results = append(results, r)

		if err := json.NewEncoder(outFh).Encode(&r); err != nil {
			return 0, fmt.Errorf("write result: %w", err)
		}
	}
	if err := scanner.Err(); err != nil && err != io.EOF {
		return 0, fmt.Errorf("scan: %w", err)
	}

	if err := writeSummary(*summary, results); err != nil {
		return 0, fmt.Errorf("write summary: %w", err)
	}

	pass := 0
	for _, r := range results {
		if r.Pass {
			pass++
		}
	}
	fmt.Printf("scenarios=%d pass=%d fail=%d\n", len(results), pass, len(results)-pass)
	fmt.Printf("results jsonl: %s\nsummary md:   %s\n", *out, *summary)

	if pass != len(results) {
		return 2, nil
	}
	return 0, nil
}

func diff(a, b []string) []string {
	bset := map[string]struct{}{}
	for _, x := range b {
		bset[x] = struct{}{}
	}
	var out []string
	for _, x := range a {
		if _, ok := bset[x]; !ok {
			out = append(out, x)
		}
	}
	return out
}

func writeSummary(path string, results []result) error {
	fh, err := os.Create(path)
	if err != nil {
		return err
	}
	defer fh.Close()
	w := bufio.NewWriter(fh)
	defer w.Flush()

	fmt.Fprintln(w, "# Phase 1 -- Hint accuracy on synthetic scenarios")
	fmt.Fprintln(w)
	fmt.Fprintln(w, "Generated by `cmd/eval-scenarios` from `milestones/eval/scenarios.jsonl` against `hints.DefaultEvaluator()`.")
	fmt.Fprintln(w)

	pass := 0
	for _, r := range results {
		if r.Pass {
			pass++
		}
	}
	fmt.Fprintf(w, "**Overall: %d / %d scenarios pass.**\n\n", pass, len(results))

	fmt.Fprintln(w, "| Scenario | Expected hints | Actual hints | Pass / Fail |")
	fmt.Fprintln(w, "|----------|----------------|--------------|-------------|")
	for _, r := range results {
		exp := joinOrDash(r.Expected)
		act := joinOrDash(r.Actual)
		status := "PASS"
		if !r.Pass {
			status = "FAIL"
		}
		fmt.Fprintf(w, "| %s | %s | %s | %s |\n", r.Scenario, exp, act, status)
	}

	fmt.Fprintln(w)
	fmt.Fprintln(w, "## Per-scenario detail")
	fmt.Fprintln(w)
	for _, r := range results {
		fmt.Fprintf(w, "- **%s**: ", r.Scenario)
		if r.Pass {
			fmt.Fprintln(w, "PASS")
			continue
		}
		var bits []string
		if len(r.Missing) > 0 {
			bits = append(bits, "missing="+strings.Join(r.Missing, ","))
		}
		if len(r.Extra) > 0 {
			bits = append(bits, "extra="+strings.Join(r.Extra, ","))
		}
		fmt.Fprintln(w, "FAIL ("+strings.Join(bits, "; ")+")")
	}
	return nil
}

func joinOrDash(xs []string) string {
	if len(xs) == 0 {
		return "-"
	}
	return strings.Join(xs, ", ")
}
